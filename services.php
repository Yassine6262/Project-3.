Bij ons staat een goede pasvorm van de schoenen voorop. Daarom hebben wij een team van specialisten in huis die u kunnen adviseren bij het vinden van de juiste schoen. Zij kijken niet alleen naar de maat, maar ook naar de breedte en vorm van uw voeten. Zo vinden wij altijd een schoen die perfect bij u past en kunt u zonder zorgen de deur uit.
