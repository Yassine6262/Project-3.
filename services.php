<h2>Reparatieservice</h2>
Heeft uw schoen een reparatie nodig? Geen probleem! Onze reparatieservice kan de meeste reparaties aan uw schoenen uitvoeren. Of het nu gaat om het vervangen van een zool, het repareren van een hak of het oprekken van een te strakke schoen, onze specialisten kunnen het voor u regelen. Zo kunt u weer snel genieten van uw favoriete paar schoenen.
